public class Circulo { 





public double area(double radio) { //encontrar el radio.



    double result = 0;

    result = 3.1416 * (radio) * (radio);

    return result;



}



public double circu(double radio) { //encontrar la circunferencia



    double result= 0;

    result = (2 * 3.1416) * (radio);

    return result; 



}

}
